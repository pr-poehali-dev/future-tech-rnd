export default function Presentation() {
  const [current, setCurrent] = useState(0);
  const navigate = useNavigate();

  const next = useCallback(() => {
    setCurrent((c) => Math.min(c + 1, slides.length - 1));
  }, []);

  const prev = useCallback(() => {
    setCurrent((c) => Math.max(c - 1, 0));
  }, []);

  useEffect(() => {
    const handler = (e: KeyboardEvent) => {
      if (e.key === "ArrowRight" || e.key === "ArrowDown" || e.key === " ") next();
      if (e.key === "ArrowLeft" || e.key === "ArrowUp") prev();
      if (e.key === "Escape") navigate("/");
    };
    window.addEventListener("keydown", handler);
    return () => window.removeEventListener("keydown", handler);
  }, [next, prev, navigate]);

  const slide = slides[current];
  const progress = ((current + 1) / slides.length) * 100;

  return (
    <div className="fixed inset-0 bg-neutral-950 text-white flex flex-col select-none overflow-hidden">
      {/* Progress bar */}
      <div className="absolute top-0 left-0 right-0 h-1 bg-neutral-800 z-20">
        <div
          className="h-full bg-orange-500 transition-all duration-500"
          style={{ width: `${progress}%` }}
        />
      </div>

      {/* Header */}
      <div className="absolute top-0 left-0 right-0 flex justify-between items-center px-6 py-4 z-10">
        <button
          onClick={() => navigate("/")}
          className="flex items-center gap-2 text-neutral-400 hover:text-white transition-colors text-sm uppercase tracking-wide"
        >
          <Icon name="ArrowLeft" size={16} />
          На сайт
        </button>
        <span className="text-neutral-500 text-sm">
          {current + 1} / {slides.length}
        </span>
      </div>

      {/* Slide content */}
      <div className="flex-1 flex items-center justify-center px-8 md:px-16 lg:px-24 pt-16 pb-20 overflow-y-auto">
        {slide.type === "title" && (
          <div className="text-center max-w-4xl py-8">
            <p className="text-orange-500 uppercase tracking-widest text-sm mb-6">{slide.note}</p>
            <h1 className="text-5xl md:text-7xl lg:text-8xl font-bold tracking-tight mb-6 leading-tight">
              {slide.title}
            </h1>
            <p className="text-xl md:text-2xl text-neutral-400 mb-8">{slide.subtitle}</p>
            {"body" in slide && slide.body && (
              <p className="text-base md:text-lg text-neutral-500 max-w-2xl mx-auto leading-relaxed">{slide.body}</p>
            )}
          </div>
        )}

        {slide.type === "content" && (
          <div className="max-w-4xl w-full py-4">
            <p className="text-orange-500 uppercase tracking-widest text-sm mb-3">{slide.label}</p>
            <h2 className="text-3xl md:text-5xl font-bold mb-8 leading-tight">{slide.title}</h2>
            <ul className="space-y-5">
              {slide.points?.map((point, i) => {
                const p = point as { head: string; desc: string };
                return (
                  <li key={i} className="flex items-start gap-4">
                    <span className="text-orange-500 mt-1 shrink-0 text-lg font-bold">—</span>
                    <div>
                      <span className="text-white text-base md:text-lg font-semibold">{p.head}</span>
                      <p className="text-neutral-400 text-sm md:text-base mt-1 leading-relaxed">{p.desc}</p>
                    </div>
                  </li>
                );
              })}
            </ul>
          </div>
        )}

        {slide.type === "stat" && (
          <div className="max-w-4xl w-full py-4">
            <p className="text-orange-500 uppercase tracking-widest text-sm mb-10 text-center">{slide.label}</p>
            <div className="grid grid-cols-2 md:grid-cols-4 gap-8 mb-10">
              {slide.stats?.map((s, i) => (
                <div key={i} className="text-center">
                  <div className="text-4xl md:text-5xl lg:text-6xl font-bold text-orange-500 mb-3">{s.value}</div>
                  <div className="text-neutral-400 text-sm md:text-base">{s.label}</div>
                </div>
              ))}
            </div>
            {"body" in slide && slide.body && (
              <p className="text-neutral-400 text-base md:text-lg leading-relaxed text-center max-w-3xl mx-auto border-t border-neutral-800 pt-8">{slide.body}</p>
            )}
          </div>
        )}

        {slide.type === "final" && (
          <div className="text-center max-w-4xl py-8">
            <h1 className="text-5xl md:text-7xl font-bold tracking-tight mb-6 leading-tight">
              {slide.title}
            </h1>
            <p className="text-xl md:text-2xl text-neutral-400 mb-8">{slide.subtitle}</p>
            {"body" in slide && slide.body && (
              <p className="text-base md:text-lg text-neutral-500 max-w-2xl mx-auto leading-relaxed mb-8">{slide.body}</p>
            )}
            <p className="text-orange-500 uppercase tracking-widest text-sm">{slide.note}</p>
          </div>
        )}
      </div>

      {/* Navigation */}
      <div className="absolute bottom-6 left-0 right-0 flex justify-center items-center gap-4 z-10">
        <button
          onClick={prev}
          disabled={current === 0}
          className="p-3 rounded-full border border-neutral-700 hover:border-neutral-400 disabled:opacity-30 disabled:cursor-not-allowed transition-all"
        >
          <Icon name="ChevronLeft" size={20} />
        </button>

        <div className="flex gap-2">
          {slides.map((_, i) => (
            <button
              key={i}
              onClick={() => setCurrent(i)}
              className={`w-2 h-2 rounded-full transition-all duration-300 ${
                i === current ? "bg-orange-500 w-6" : "bg-neutral-600 hover:bg-neutral-400"
              }`}
            />
          ))}
        </div>

        <button
          onClick={next}
          disabled={current === slides.length - 1}
          className="p-3 rounded-full border border-neutral-700 hover:border-neutral-400 disabled:opacity-30 disabled:cursor-not-allowed transition-all"
        >
          <Icon name="ChevronRight" size={20} />
        </button>
      </div>
    </div>
  );
}